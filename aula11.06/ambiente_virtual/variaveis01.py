# variaveis são usadas para salvar algo na  memoria do computador
# o sinal de = é utilizado para atribuição de valores
# os valores de uma variavel são mutaveis
var01 = "alguma coisa"
print(var01)
var01 = "outra coisa"
print(var01)
var01 = 10
print(var01)
var01 = "A" + "B" + "C"
print(var01)
var01 = "A" * 10
print(var01)
var02 = 10 * "Jeferson "
print(var02)